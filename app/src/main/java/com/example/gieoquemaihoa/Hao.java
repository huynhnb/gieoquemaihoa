public class Hao {
	int value;
	// 0 = am
	// 1 = duong
	String display;
	
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
		if (value == Am.value) {
			display = Am.display;
		} else {
			display = Duong.display;
		}
	}
	public String getDisplay() {
		if (this.value == Am.value) {
			return Am.display;
		} else {
			return Duong.display;
		}
	}
	
	
}
